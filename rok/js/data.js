$(document).ready(function(){
    $('#cityhall_level_sel').on('change',function(){
        var kind_buliding_idx = $('#buliding_kind option:selected').val();
        var val = $(this).val();
        if(val != -1){
        
            allFood = 0;
            allWood = 0;
            allStone = 0;
            allGold = 0;
            aAleadyBuild = [];

            $('.build_all_need').html('');
            $('.cityhall_result').css({display:'block'});


            buildNeedCal(aBulingList[kind_buliding_idx][val],0);

            $('.res_all_food_amount').text(number_format(allFood));
            $('.res_all_wood_amount').text(number_format(allWood));
            $('.res_all_stone_amount').text(number_format(allStone));
        }
    });
    
    $('#buliding_kind').on('change',function(){
        $('.build_all_need').html('');
        $('.cityhall_result').css({display:'none'});
        $('#cityhall_level_sel option').eq(0).prop('selected',true);
    });
    //$('.build_all_need')
});

function buildNeedCal(target,treeLevel){
    
    if(!aAleadyBuild.includes(target)){
        if(target.f != undefined){
            allFood += target.f;
        }
        if(target.w != undefined){
            allWood += target.w;
        }
        if(target.s != undefined){
            allStone += target.s;
        }
        if(target.g != undefined){
            allGold += target.g;
        }
        
        aAleadyBuild.push(target);
    }
    
    allText = '';
    if(treeLevel == 0){
        allText += '<li class="super">';
        allText += '    <div class="build_all_need_info">';
        allText += '        <p>'+target.name+' Lv.'+target.level+'</p>';
        allText += '    </div>';
        allText += '    <div class="build_all_resource">';
        allText += htmlAdd(target.f,'item_food.png');
        allText += htmlAdd(target.w,'item_wood.webp');
        allText += htmlAdd(target.s,'item_stone.webp');
        allText += '    </div>';
        allText += '</li>';

        $('.build_all_need').append(allText);
    }
    
    allText = '';
    if(target.need.length > 0){
        for(var i=0; i<target.need.length; i++){
            allText += '<li>';
            allText += '    <div class="build_all_need_info">';
            
            for(var q=0; q<treeLevel; q++){
                allText += '    <img src="./images/arrows.png">';
            }
            
            allText += '        <p>'+target.need[i].name+' Lv.'+target.need[i].level+'</p>';
            allText += '    </div>';
            allText += '    <div class="build_all_resource">';
            allText += htmlAdd(target.need[i].f,'item_food.png');
            allText += htmlAdd(target.need[i].w,'item_wood.webp');
            allText += htmlAdd(target.need[i].s,'item_stone.webp');
            allText += '    </div>';
            allText += '</li>';
        
            $('.build_all_need').append(allText);
            
            buildNeedCal(target.need[i],treeLevel+1);
            
        }
    }
    
}

function htmlAdd(r,name){
    if(r != undefined){
        return '<p><img src="./images/'+name+'"> '+number_format(r)+'</p>';
    }else{
        return '';
    }
}

function number_format(num) {
    if(num/100000000 > 1){
        return Math.floor(num/100000000)+'억 '+(num%100000000).toString().replace(/\B(?=(\d{3})+(?!\d))/g, ",");
    }else{
        return num.toString().replace(/\B(?=(\d{3})+(?!\d))/g, ",");
    }
}
